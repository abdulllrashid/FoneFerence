package com.example.training.myapplication;

import android.transition.Slide;

public class Slides {

    private String slideTitle, slideurl, slidedesc;

    public Slides(String slideTitle, String slideurl, String slidedesc) {
        this.slideTitle = slideTitle;
        this.slideurl = slideurl;
        this.slidedesc = slidedesc;
    }

    public String getSlideTitle() {
        return slideTitle;
    }

    public void setSlideTitle(String slideTitle) {
        this.slideTitle = slideTitle;
    }

    public String getSlideurl() {
        return slideurl;
    }

    public void setSlideurl(String slideurl) {
        this.slideurl = slideurl;
    }

    public String getSlidedesc() {
        return slidedesc;
    }

    public void setSlidedesc(String slidedesc) {
        this.slidedesc = slidedesc;
    }

    public Slides() {

    }

}

